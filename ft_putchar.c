#include "ft_printf.h"

int	ft_putchar(char c)
{
	size_t	count;

	count = 0;
	if (c == '\001' || c == '\002' || c == '\007'
		|| c == '\v' || c == '\010' || c == '\f' || c == '\r' || c == '\n')
		return (-1);
	count += write(1, &c, 1);
	return (count);
}
